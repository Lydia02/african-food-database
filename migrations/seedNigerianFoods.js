// ─── Find Nigeria's doc ID ─────────────────────────────────────
const getNigeriaId = async () => {
  const snapshot = await db.collection(COLLECTIONS.COUNTRIES)
    .where('code', '==', 'NG')
    .limit(1)
    .get();
  if (snapshot.empty) throw new Error('Nigeria not found — run npm run seed:countries first');
  return { id: snapshot.docs[0].id, name: snapshot.docs[0].data().name };
};

