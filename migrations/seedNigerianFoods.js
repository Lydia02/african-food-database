// ─── Seeder logic ──────────────────────────────────────────────
export const seedNigerianFoods = async () => {
  const nigeria = await getNigeriaId();
  console.log(`  📍 Nigeria found: ${nigeria.id}`);

  const collectionRef = db.collection(COLLECTIONS.FOODS);

  // Delete existing Nigerian foods only
  const existing = await collectionRef.where('countryId', '==', nigeria.id).get();
  if (!existing.empty) {
    const BATCH_SIZE = 500;
    for (let i = 0; i < existing.docs.length; i += BATCH_SIZE) {
      const batch = db.batch();
      existing.docs.slice(i, i + BATCH_SIZE).forEach((doc) => batch.delete(doc.ref));
      await batch.commit();
    }
    console.log(`  🗑️  Cleared ${existing.size} existing Nigerian foods`);
  }

  const foodsData = makeFoods(nigeria.id, nigeria.name);

  // Insert in batches
  const BATCH_SIZE = 500;
  const results = [];
  for (let i = 0; i < foodsData.length; i += BATCH_SIZE) {
    const batch = db.batch();
    const chunk = foodsData.slice(i, i + BATCH_SIZE);
    for (const data of chunk) {
      const formatted = formatFood(data);
      const docRef = collectionRef.doc();
      batch.set(docRef, formatted);
      results.push({ id: docRef.id, ...formatted });
    }
    await batch.commit();
  }

  // Update food count on Nigeria
  const countryRef = db.collection(COLLECTIONS.COUNTRIES).doc(nigeria.id);
  await countryRef.update({ foodCount: results.length });

  return results;
};

// ─── Run standalone ────────────────────────────────────────────
const isMainModule = process.argv[1]?.replace(/\\/g, '/').endsWith('seedNigerianFoods.js');

if (isMainModule) {
  console.log('🇳🇬 Seeding Nigerian foods…\n');
  seedNigerianFoods()
    .then((foods) => {
      console.log(`\n✅ Seeded ${foods.length} Nigerian foods!`);
      console.log('\nFoods seeded:');
      foods.forEach((f, i) => console.log(`  ${i + 1}. ${f.name} — ${f.region}`));
      process.exit(0);
    })
    .catch((err) => {
      console.error('❌ Error:', err.message);
      process.exit(1);
    });
}
